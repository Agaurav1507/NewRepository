package Loops;

public class whileloop1 {

	public static void main(String[] args) {
		int i=100;
		while(i>=1) {
			
			i--;
			
		System.out.println(i);
		}
		
		char alpha='a';
		while(alpha<'z') {
			
			alpha++;
			System.out.println(alpha);
		}
		
		char bets='z';
		while(bets>='a') {
			bets--;
			
			System.out.println(bets);
		}

	}

}
